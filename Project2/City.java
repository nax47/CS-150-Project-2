/**
 * City class
 * 
 * @author Nakul Talwar
 */
public class City
{
    //Name of the city
    private String name;
    /**
     * Default constructor.
     */
    public City(String name)
    {
        this.name = name;
    }

    /**
     * Method to retrieve city name.
     * 
     * @return Name of city.
     */
    public String getName()
    {
        return name;
    }
}
